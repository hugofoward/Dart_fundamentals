main() {
/* 
  We can use literal values or dinamic values that is variables
  Podemos utilizar valores literais ou valores dinâmicos que são as variáveis
*/

// Literal values
  /// Valores literais
  print('value from literal values:');
  print(1 + 2 * 4);

// Dynamic values(variables)
  /// Valores Dinâmicos(variáveis)
  int a = 2;

// assigning a initial value
  /// atribuindo um valor inicial em sua definição
  double b = 3.1314;
// modifying for a new value
  /// modificando o valor inicial para um novo valor
  b = 3.1415;

// Printing the variables
  /// Impressão das variáveis
  print('value from variable:');
  print(a);
  print(b);
  print(a * b);
}
